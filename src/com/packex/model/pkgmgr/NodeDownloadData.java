package com.packex.model.pkgmgr;

public class NodeDownloadData {
    Integer downloads;
    String day;
    String start;
    String end;

    public Integer getDownloads() {
        return this.downloads;
    }
    
    public String getDay() {
        return this.day;
    }
    
    public String getStart() {
        return this.start;
    }
    
    public String getEnd() {
        return this.end;
    }
}
