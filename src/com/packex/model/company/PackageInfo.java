package com.packex.model.company;

public class PackageInfo {
    String language;
    String category;
    String name;
    
    public PackageInfo() { }
    
    public String getLanguage() {
        return this.language;
    }
    
    public String getCategory() {
        return this.category;
    }
    
    public String getName() {
        return this.name;
    }
}
